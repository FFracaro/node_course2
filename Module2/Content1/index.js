/**
 * > maior
 * < menor
 * >= maior igual
 * <= menor igual
 * == igual -> checa valor
 * === igualdade estrita -> checa valor e tipo
 * != diferença -> checa valor
 * !== diferença estrita -> checa valor e tipo
 */

